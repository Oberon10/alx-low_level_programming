#include <stdio.h>

/**
 * add - sum of two numbers
 * @a: the first number to be added
 * @b: the second number to be added
 * Return: the result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract of two numbers
 * @a: the first number to be subtracted
 * @b: the second number to  be subtracted
 * Return: the result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply of two numbers
 * @a: the first number to be multiplied
 * @b: the second number to  be muliplied
 * Return: the result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide of two numbers
 * @a: the first number to be divide
 * @b: the second number to  be divide
 * Return: the result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */

int mod(int a, int b)
{
	return (a % b);
}
